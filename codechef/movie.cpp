#include <bits/stdc++.h>
using namespace std;

int main()
{
	int t;
	cin>>t;
	while(t--)
	{
		long long int  n;
		cin>>n;
		if(n<=250000)
			cout<<n;
	    else if(n>250000 && n<=500000)
			cout<<n- ((n-250000)*0.05);
		else if(n>500000 && n<=750000)
			cout<<n-((250000)*0.05 + (n-500000)*0.1);
		else if (n>750000 && n<=1000000)
            cout<<n-((250000)*0.05 + (250000)*0.1 + (n-750000)*0.15);
        else if(n>1000000 && n<=1250000)
        	cout<<n-((250000)*0.05 + (250000)*0.1 + (250000)*0.15 + (n-1000000)*0.2);
        else if(n>1250000 && n<=1500000)
        	cout<<n-((250000)*0.05 + (250000)*0.1 + (250000)*0.15 + (250000)*0.2 + (n-1250000)*0.25);
        else if(n>1500000)
        	cout<<n-((250000)*0.05 + (250000)*0.1 + (250000)*0.15 + (250000)*0.2 + (250000)*0.25 + (n-1500000)*0.3);
		
		cout<<endl;
	}
	return 0;
}